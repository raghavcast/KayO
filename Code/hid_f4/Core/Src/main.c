void drive_column(void) {
	col = (col + 1) & 0x3;
	GPIOB->BSRR = 0xf << 16;
	GPIOB->BSRR = 0x1 << (col);
}

uint8_t read_rows(void) {
	return ((GPIOB->IDR & 0xf0) >> 4) & 0xf;
}

